export function getAgencyById(id: string): Agency | undefined {
  return agencies.find((agency) => agency.id === id);
}

export function getTopAgencies(limit: number = 3): Agency[] {
  return [...agencies]
    .sort((a, b) => b.rating - a.rating)
    .slice(0, limit);
}

export function getAllAgencies(): Agency[] {
  return [...agencies].sort((a, b) => b.rating - a.rating);
}
